// Get all clubs as flat array
export const getAllClubs = () => {
  const allClubs = []
  Object.values(FC26_CLUBS_BY_LEAGUE).forEach(leagueData => {
    leagueData.clubs.forEach(club => {
      allClubs.push({
        ...club,
        league: Object.keys(FC26_CLUBS_BY_LEAGUE).find(
          key => FC26_CLUBS_BY_LEAGUE[key] === leagueData
        ),
        country: leagueData.country
      })
    })
  })
  return allClubs
}

// Get clubs by league
export const getClubsByLeague = (leagueName) => {
  const league = FC26_CLUBS_BY_LEAGUE[leagueName]
  if (!league) return []
  return league.clubs.map(club => ({
    ...club,
    league: leagueName,
    country: league.country
  }))
}

// Get clubs by country
export const getClubsByCountry = (countryName) => {
  const result = []
  Object.entries(FC26_CLUBS_BY_LEAGUE).forEach(([leagueName, leagueData]) => {
    if (leagueData.country === countryName) {
      leagueData.clubs.forEach(club => {
        result.push({
          ...club,
          league: leagueName,
          country: leagueData.country
        })
      })
    }
  })
  return result
}

// Get all unique countries
export const getAllCountries = () => {
  const countries = new Set()
  Object.values(FC26_CLUBS_BY_LEAGUE).forEach(data => {
    countries.add(data.country)
  })
  return Array.from(countries)
}

// Search clubs
export const searchClubs = (query) => {
  const results = []
  Object.entries(FC26_CLUBS_BY_LEAGUE).forEach(([leagueName, leagueData]) => {
    leagueData.clubs.forEach(club => {
      if (club.name.toLowerCase().includes(query.toLowerCase()) ||
          leagueData.country.toLowerCase().includes(query.toLowerCase())) {
        results.push({
          ...club,
          league: leagueName,
          country: leagueData.country
        })
      }
    })
  })
  return results
}

// Get top rated clubs
export const getTopRatedClubs = (limit = 20) => {
  const allClubs = getAllClubs()
  return allClubs.sort((a, b) => b.rating - a.rating).slice(0, limit)
}

